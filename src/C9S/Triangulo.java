package C9S;

public class Triangulo {
    private String color;
    private int tamaño;

    public Triangulo(String color, int tamaño) {
        this.color = color;
        this.tamaño = tamaño;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getTamaño() {
        return tamaño;
    }

    public void setTamaño(int tamaño) {
        this.tamaño = tamaño;
    }
}
